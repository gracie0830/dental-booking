<h1>Tooth Extraction</h1>
	<p>If a tooth has been broken or damaged by decay, your dentist will try to fix it with a filling,
	crown or other treatment. Sometimes, though, there's too much damage for the tooth to be repaired.
	In this case, the tooth needs to be extracted. A very loose tooth also will require extraction
	if it can't be saved, even with bone replacement surgery (bone graft).</p>

	<p>Here are other reasons:
	<ul>
		<li>Some people have extra teeth that block other teeth from coming in.</li>
		<li>Sometimes baby teeth don't fall out in time to allow the permanent teeth to come in.</li>
		<li>People getting braces may need teeth extracted to create room for the teeth that are being moved into place.</li>
		<li>People receiving radiation to the head and neck may need to have teeth in the field of radiation extracted.</li>
		<li>People receiving cancer drugs may develop infected teeth because these drugs weaken the immune system.
		Infected teeth may need to be extracted.</li>
		<li>Some teeth may need to be extracted if they could become a source of infection after an organ transplant.
		People with organ transplants have a high risk of infection because they must take drugs that decrease or suppress the immune system.</li>
		<li>Wisdom teeth, also called third molars, are often extracted either before or after they come in.
		They commonly come in during the late teens or early 20s. They need to be removed if they are decayed, cause pain or have a cyst or infection.
		These teeth often get stuck in the jaw (impacted) and do not come in. This can irritate the gum, causing pain and swelling. In this case, the tooth must be removed.
		If you need all four wisdom teeth removed, they are usually taken out at the same time.</li>
	</ul>
	</p>

	<h1>How It's Done</h1>
<p>There are two types of extractions:
<ul>
	<li>A simple extraction is performed on a tooth that can be seen in the mouth.
	General dentists commonly do simple extractions. In a simple extraction,the dentist loosens the tooth
	with an instrument called an elevator. Then the dentist uses an instrument called a forceps to remove
	the tooth.</li>
	<li>A surgical extraction is a more complex procedure. It is used if a tooth may have broken off at the gum line
	or has not come into the mouth yet. Surgical extractions commonly are done by oral surgeons. However, they are
	also done by general dentists. The doctor makes a small incision (cut) into your gum. Sometimes it's necessary
	to remove some of the bone around the tooth or to cut the tooth in half in order to extract it.
	Most simple extractions can be done using just an injection (a local anesthetic). You may or may not receive
	drugs to help you relax. For a surgical extraction, you will receive a local anesthetic, and you may also have
	anesthesia through a vein (intravenous). Some people may need general anesthesia. They include patients with
	specific medical or behavioral conditions and young children.</li>
	</ul>
	</p>
<p>
	If you are receiving conscious sedation, you may be given steroids as well as other medicines in your IV line.
	The steroids help to reduce swelling and keep you pain-free after the procedure.<br><br>
	During a tooth extraction, you can expect to feel pressure, but no pain. If you feel any pain or pinching,
	tell your doctor.
</p>

