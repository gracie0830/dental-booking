<h1>DENTURES</h1>
	<p>
		Dentures are custom-made replacements for missing teeth and can be taken out and put back into your mouth.
		While dentures take some getting used to, and will never feel exactly the same as natural teeth,
		today's dentures are natural looking and more comfortable than ever.
	</p>

	<h1>Benefits</h1>
	<p>
		Replacing missing teeth will help to improve your appearance and smile. Without support from the denture,
		facial muscles sag, making a person look older. What’s more,
		dentures will help you eat and speak more comfortably —
		things that people often take for granted until their natural teeth are lost.
	</p>

	<h1>Care</h1>
	<p>
		Over time, your denture will need to be relined, remade or rebased as a result of normal wear.
		Rebasing requires making a new base, while keeping the existing denture teeth.
		As you age, your mouth naturally changes shape. These changes cause your dentures to loosen,
		making chewing difficult and irritating your gums.
		At a minimum, you should see your dentist annually for a checkup.<br><br>

		Here are tips to help you care for dentures:

		<ul>
			<li>When handling dentures, stand over a folded towel or basin of water.
			Dentures are delicate and may break if dropped.</li>
			<li>Don't let dentures dry out. Place them in a denture cleanser soaking solution or in plain water
			when you're not wearing them.
			Never use hot water, which can cause dentures to warp.</li>
			<li>Brushing dentures daily will remove food and dental plaque, and help prevent them from becoming stained.
			An ultrasonic cleaner may be used to care for dentures, but it does not replace a thorough daily brushing.</li>
			<li>Brush your gums, tongue and palate every morning with a soft-bristled brush before you insert dentures.
			This stimulates circulation in your tissues and helps remove plaque.</li>
			<li>See your dentist if dentures break, chip, crack, or become loose.
			Don't be tempted to adjust them yourself — this can damage them beyond repair.</li>
		</ul>
	</p>

	<h1>Procedure</h1>
	<p>
		<span>Full Denture:</span><br>
		A conventional full denture is made and placed in a patient's mouth after the remaining teeth are removed
		and tissues have healed, which may take several months.<br><br>

		An immediate complete denture is inserted as soon as the remaining teeth are removed.
		The dentist takes measurements and makes models of the patient's jaws during a preliminary visit.
		With immediate dentures, the denture wearer does not have to be without teeth during the healing period.<br><br>

		<span>Partial Denture:</span><br>
		A partial denture rests on a metal framework that attaches to your natural teeth.
		Sometimes crowns are placed on some of your natural teeth and serve as anchors for the denture.
		Partial dentures offer a removable alternative to bridges.
	</p>

