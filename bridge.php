<h1>Dental Bridge</h1>
	<p>Dental Bridges gap the space between one or more missing teeth. Generally they are made of two or more crowns on either side of the missing teeth. These teeth that support the gap on either end are called abutment or anchoring teeth. The false teeth on the gap are made of special alloy, gold or porcelain. Bridges can be supported by both natural teeth as well as the dental implant teeth.<br><br>

	Dental bridges help to:

	<ol>
		<li>Restore the original smile</li>
		<li>Help maintain the shape of your jaw and face</li>
		<li>Prevent the shift of other teeth</li>
		<li>Help you chew and speak properly</li>
	</ol>
	</p>

	<h1>Post Operative Instructions</h1>
	<p>
		<ul>
		<li>Avoid eating sticky/ hard foods , and also chewing gum. If the temporary crown comes off before the next appointment, place the temporary back on the tooth and call our office. It is very essential to re- cement the temporary crown back, because this maintains the space needed for the custom-made crown.</li>
		<li>Brushing and flossing is essential with the placement of the temporary crown. While flossing do not pull the floss towards the top of the tooth, rather insert the floss and pull it out sideways.</li>
		<li>With the temporary crown and with the permanent crown, you may experience some discomfort due to irritation of the area during the treatment procedures. You may also experience sensitivity to cold or pressures.</li>
		<li>After the new permanent crown is in place you may need a few days to adjust to it. If you feel the bite is not correctly balanced, please call for an appointment and we can adjust it.</li>
		<li>Care for your new crown with proper brushing and flossing. It is especially important to keep the edge of the crown clean at the gum line.</li>
		<li>If you have a bridge placed make sure to floss under the bridge where you there is no natural teeth, as food accumulates in that area easily and can cause bad breath or an infection.</li>
		<li>A floss threader or Water-pik can be used to clean under the dental bridge.</li>
	</ul>
	</p>

	
