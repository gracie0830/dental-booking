<h1>What is Root Canal Treatment Procedure?</h1>
	<p>
		Dental Root Canal is a Dental Procedure, which saves the Tooth by performing a simple repair procedure.
		Often the Pulp of the Tooth suggested for this procedure is badly infected or decayed.
		Pulp of the tooth is composed of blood vessels and Nerves.
	</p>

	<h1>How long is the Root Canal Procedure?</h1>
	<p>
		It will take between 1-2 hours for your Dentist or a Dental Specialist,
		who does Root Canal's to perform this procedure.
	</p>

	<h1>What is done in the Root Canal Therapy?</h1>
	<p>
		Your Dentist will remove the Infected Pulp.
		He will also clean and seal the inside of the tooth with a special sealing material.
	</p>

	<h1>What Causes damage to the Tooth's Nerve and Pulp?</h1>
	<p>
		Tooth Decay and repeated dental procedures in a Tooth, crack or chip, injury etc can cause the damages.
	</p>

	<h1>Why do I need to remove the Pulp?</h1>
	<p>
		When your Teeth pulp becomes damaged, bacteria begins to spread and multiply within the pulp chamber.
		This can cause tooth abscess and other Mouth Infections. This can cause Tooth pain and discomfort.
		If not treated early, it may result in the loss of tooth by extraction.
		Root Canal can save the Tooth Extraction.
	</p>

	<h1>Do I need a Dental Cap after the Treatment?</h1>
	<p>
		Dental Crown (Dental Caps) protects the Tooth after Root Canal Procedure.
		There is no blood supply to the Tooth after the Treatment.
		Dental Crown will prevent the Tooth from fractures and Extraction.
	</p>

	<h1>Post Operative Instructions</h1>
	<p>
		<ul>
			<li>Avoid any chewing until anesthesia has completely worn off.</li>
			<li>It is normal to experience some discomfort when chewing for several days following a root canal appointment.
			To control discomfort take pain medication as recommended.</li>
			<li>Continue to take antibiotics for the indicated length of time, if prescribed.
			You should continue using antibiotics even if there is no symptoms and signs of infection.</li>
			<li>To protect the tooth and keep temporary in place, avoid eating sticky foods.</li>
			<li>Avoid hard foods especially gum, and if possible chew only on the opposite side of the mouth.</li>
			<li>Between appointments it is common for a small portion of your temporary filling to wear away or break off.
			But if the filling falls off or comes out entirely, please call us so that we can put new temporary filling.</li>
			<li>It is important to continue to brush and floss normal.</li>
			<li>Usually the last step after the treatment is the placement of crown on the tooth.</li>
		</ul>
	</p>

