<h1>Veneers</h1>
	<p>Veneers is a type of restorative material , that is placed on teeth to improve the esthetics in case of stained or discolored teeth or to protect damaged tooth surface, like a chip or crack. Porcelain material is like glass and give a very natural appearance to your teeth by reflecting light like a natural enamel. The teeth marked for porcelain veneers are minimally reduced thereby preserving maximum tooth structure. <br><br>

	An impression of the teeth preparation is taken and sent to the lab for processing. Porcelain veneers are bonded to the tooth structure with a resin cement. <br><br>

	Porcelain veneers are known for providing brighter, whiter and <span>beautiful smiles</span>!
	</p>

	<h1>What Types of Problems Do Dental Veneers Fix?</h1>
	<p>
		This Cosmetic Dental procedure is used to fix:
			<ul>
				<li>discolored teeth. Discoloration could be due to water quality, drug use, individual habits like smoking etc.</li>
				<li>Worn down or chipped tooth</li>
				<li>Irregular, misaligned and teeth gaps etc.</li>
			</ul>
	</p>

	<h1>Benefit</h1>
	<p>
		For teeth that are severely discolored, chipped or misshapen, veneers create a durable and pleasing smile. Plus, veneers are difficult to stain, making them popular for people seeking a perfect smile.
	</p>

	
